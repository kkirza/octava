				for (let i = 0; i < c420.length; i++) {
					c420[i].style.display = 'none';
				}
				for (let i = 0; i < more.length; i++) {
					more[i].style.display = 'block';
				}

			} else if (window.innerWidth <= 420) {
				for (let i = 0; i < more.length; i++) {
					more[i].style.display = 'none';
				}
